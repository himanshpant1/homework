import numpy as np

random_array = np.random.rand(4, 2)
print("4x2 Random Array:")
print(random_array)

empty_array = np.empty((4, 2))
print("\n4x2 Empty Array:")
print(empty_array)

zeros_array = np.zeros((3, 5))
print("\n3x5 Zeros Array:")
print(zeros_array)

ones_array = np.ones((4, 3, 2))
print("\n4x3x2 Ones Array:")
print(ones_array)
